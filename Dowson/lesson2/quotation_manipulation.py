quote = "Трудно представить современную разработку без контейнеризации"

print( "UPPER:\t", quote.upper() )
print( "lower:\t", quote.lower() )
print( "titel:\t", quote.title() )
print( "replace:", quote.replace("Трудно", "Легко"))

# Extend
print( "swapcase:", quote.swapcase() )
print( "capitalize:", quote.capitalize() )
print( "strip:\t", quote.strip() )